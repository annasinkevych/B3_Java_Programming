package day16_loops;

public class PrintNumber {

    public static void main(String[] args) {
        int num = 2;

        while(num<=100)
        {
            System.out.println(num);
            num +=2;
        }
        int i = 1;
        while (i <+100)
        {
            System.out.println(i);
            i++;
        }
    }
}
