package day03_comments_escape_sequence;

public class TabAndNextLine {
    public static void main(String[] args) {
        System.out.println("\tThis is a sentence");// \t - 1 tab is equal to 4 spaces
        System.out.println("This is a sentence");
        System.out.println("This is a sentence");
        //\t - equal to 4 spaces

        System.out.println(" 1) Got to store\n 2) Grab a bread\n 3) Pay Cashier");

    }
}
