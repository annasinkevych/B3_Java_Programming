package day17_loops;

public class DoWhileExample {

    public static void main(String[] args) {
        int a = 0;
        do{
            System.out.println(a);
            a++;

        } while(a<=5);

       int a2 = 0;

       do {
           System.out.println(a2);
           a2+=5;
       }while(a2<=15);

       int a3 = 0;
        do {
            System.out.println(a3);

        }while(a3 == 10);
    }
}
